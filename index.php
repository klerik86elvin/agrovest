<script>
  var a = 0;
  $(window).load(function() {

    var oTop = $('.main_product_glasscards').offset().top - window.innerHeight;
    var sc = oTop + 100;
    if (a == 0 && $(window).scrollTop() > sc) {
      $('.counter-value').each(function() {
        var $this = $(this),
          countTo = $this.attr('data-count');
        $({
          countNum: $this.text()
        }).animate({
            countNum: countTo
          },

          {

            duration: 6000,
            easing: 'swing',
            step: function() {
              $this.text(Math.floor(this.countNum));
            },
            complete: function() {
              $this.text(this.countNum);
              //alert('finished');
            }

          });
      });
      a = 1;
    }

  });
</script>