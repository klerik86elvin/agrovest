<script>
    $(document).ready(function() {
        $.ajaxSetup({
            headers: {
                'X-CSRF-TOKEN': $('meta[name="csrf-token"]').attr('content')
            }
        });
    });
    $(document).on('keyup', '#filter_inp', function() {


                $.ajax({
                    url: "{{ route('get_product') }}",
                    type: 'get',
                    data: {
                        product_name: $(this).val()
                    },
                    dataType: 'json',
                    success: function(response) {

                        var len = response['products'].length;
                        if (len > 0) {
                            $('.product_page_list .row').empty();
                            for (var i = 0; i < response['products'].length; i++) {
                                $('.product_page_list .row').append('<div class="col-lg-3 col-md-4 col-6"><div class="product_box"><a href="product_inner.php"><div class="product_img"><img src="assets/images/ ' + response['products'][i].image + '" alt=""></div><div class="product_name"><p>' + response['products '][i].name + ' < /p><span>' + response['products'][i].description + '</span > < /div></a > < /div></div > ');
                                    }
                                }
                            },
                            error: function() {

                                alert('Product not found  :(');

                            }
                        });



                });
</script>
